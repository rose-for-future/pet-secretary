// 语音路由：判断一句听写文字是否需要走"文字大脑"(qwen-plus，能调待办/记忆工具)。
//   needsBrain=true  → 走大脑（慢一点但能可靠执行增删改/记忆）
//   needsBrain=false → 纯闲聊，走 Omni 端到端快聊（说完直接出声）
// 取舍：宁可把含任务信号的句子误送大脑（大脑没工具可调时也会闲聊），也别把"要办事"漏判成闲聊。

// 命中任一关键词 = 很可能在操作待办/记忆/时间 → 走大脑。
// 刻意【不含】裸时间词(今天/明天/晚上)：它们也常出现在闲聊里（「今天心情不错」），
// 而真正的排程句几乎都另带动作词（提醒/改到/叫我…），靠动作词判更准、误伤更少。
const TASK_RE = new RegExp(
  [
    // 创建 / 记事 / 提醒
    '提醒', '待办', '日程', '备忘', '闹钟', '倒计时', '计时',
    '记一下', '记下', '记住', '记得我', '别忘', '叫我',
    // 删除 / 完成
    '删', '取消', '清空', '清掉', '完成', '标记', '做完', '搞定', '办好',
    // 修改
    '改到', '改成', '改为', '推迟', '提前', '挪到', '换成',
    // 记忆移除
    '忘掉', '忘了', '别记',
    // 查询
    '有哪些', '念一下', '列一下', '安排',
    // 明确的相对 / 周期时间信号（裸时间词不算）
    '分钟后', '小时后', '几点', '点钟', '每天', '每周', '每月', '工作日'
  ].join('|')
)

/** 这句话是否需要交给文字大脑处理（含待办/记忆/排程信号）。否则当闲聊走端到端快聊。 */
export function needsBrain(text: string): boolean {
  return TASK_RE.test(text.trim())
}
