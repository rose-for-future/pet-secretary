/**
 * 真话底线（代码强制，不靠模型自觉）：
 * 当大脑没有调用任何工具、只回了一句闲聊文字时，这句话**绝不能声称完成了某个待办操作**
 * （"已设好/已删除/清完了"之类）——因为没调工具就等于没做，说了就是假话。
 * 这里检测这种"未执行却声称完成"的话术，调用方拦截后改用老实的回复。
 */
const DONE_RE =
  /(已经|已)[^。！？\n]{0,8}(设|定|加|记|删|清空|清除|改|完成|搞定|建|存|标记)|(设|定|加|记|删|清|改|建|存)(好|上|下|掉|完)\s*(了|啦|咯)|完成了|搞定了|清完\s*(了|啦)?|清空\s*(了|啦)|记下了|加上了|帮你(设|定|加|记|删|清|改|建)/

/** 这句（没调工具的）回复是否在假装已经做完了某个操作。 */
export function claimsCompletion(text: string): boolean {
  return DONE_RE.test(text)
}
